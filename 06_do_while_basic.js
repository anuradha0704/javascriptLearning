// WAP to print number from 0 to  5
console.warn("display number============================");
var result="";
var count = 0; // initialization
do {
   //console.log(count);
   result= result+" " + count;
   count ++;   // update 
} while (count<=5); // Condition
console.log(`Numbers are:-${result}`);


